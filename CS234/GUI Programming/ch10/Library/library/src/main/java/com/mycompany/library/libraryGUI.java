    private void setColumnWidth(int[] width)
    {
        tblBooklist.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        int count = tblBooklist.getColumnCount()-1;
        for (int i=0; i<=count; i++)
        {
            TableColumn column = tblBooklist.getColumnModel().getColumn(i);
            column.setPreferredWidth(width[i]);
        }
    }
    
    private void loadBooks()
    {
        String bookTitle = "";
        for (int i=1; i<=8; i++)
        {
            if(i==1) bookTitle="HTML for web pages";
            if(i==2) bookTitle="Windows 8 reference";
            if(i==3) bookTitle="Microsoft Office 2013";
            if(i==4) bookTitle="3D graphics techniques";
            if(i==5) bookTitle="Object Oriented C++ programming";
            if(i==6) bookTitle="Using JAVA";
            if(i==7) bookTitle="SQL Databases";
            if(i==8) bookTitle="Assembly language for electronics";
            
            Book.bookObject[i] = new Book(i, bookTitle);
        }
    }
    
    private void displayBooks()
    {
        for (int i=1; i<=8; i++)
        {
            tblBooklist.getModel().setValueAt(i, i-1, 0);
            tblBooklist.getModel().setValueAt(Book.bookObject[i].getTitle(), i-1, 1);
            if (Book.bookObject[i].getBorrowed() == false)
            {
                tblBooklist.getModel().setValueAt("not on loan", i-1, 2);
            }
            else
            {
                tblBooklist.getModel().setValueAt("on loan to " +
                                                    Book.bookObject[i].getBorrower(), 
                                                    i-1, 2);
            }
        }
    }

